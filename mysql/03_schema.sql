   ID_TIPO_ARTICOLO   int(10)      NOT NULL,
   ID_LINGUA          int(10)      NOT NULL,
   DESCRIZIONE        varchar(255),
   CONSTRAINT PK_CMS_TIPI_ARTICOLO_DESC PRIMARY KEY (ID_TIPO_ARTICOLO, ID_LINGUA),
