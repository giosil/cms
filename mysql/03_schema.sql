CREATE TABLE CMS_TIPI_ARTICOLO (
   ID_TIPO_ARTICOLO   int(10)      NOT NULL,
   CODICE             varchar(255) NOT NULL,
   ATTIVO             varchar(1)   NOT NULL,
   CONSTRAINT PK_CMS_TIPI_ARTICOLO PRIMARY KEY (ID_TIPO_ARTICOLO) );

